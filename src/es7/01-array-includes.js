// Declaración de un array llamado 'numbers'
let numbers = [1, 3, 4, 6, 7, 8];

// Verificar si el número 4 está presente en el array 'numbers'
console.log(numbers.includes(4)); // true, porque 4 está en el array

// Verificar si el número 9 está presente en el array 'numbers'
console.log(numbers.includes(9)); // false, porque 9 no está en el array

// Declaración de un array llamado 'list'
const list = ['oscar', 'David', 'Ana'];

// Verificar si 'oscar' está presente en el array 'list'
console.log(list.includes('oscar')); // true, porque 'oscar' está en el array

// Verificar si 'Oscar' está presente en el array 'list'
console.log(list.includes('Oscar')); // false, porque 'Oscar' no está en el array







