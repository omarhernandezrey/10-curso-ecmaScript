// Declaración de un objeto llamado 'countries' con pares clave-valor
const countries = {
    MX: 'Mexico',
    CO: 'Colombia',
    CL: 'Chile',
    PE: 'Peru'
};

// Utilización de 'Object.values()' para obtener un array de los valores del objeto
console.log(Object.values(countries));

// Resultado:
// ['Mexico', 'Colombia', 'Chile', 'Peru']
