// Declaración de un objeto llamado 'countries' con pares clave-valor
const countries = {
    MX: 'Mexico',
    CO: 'Colombia',
    CL: 'Chile',
    PE: 'Peru'
};

// Utilización de 'Object.entries()' para convertir el objeto en una matriz de pares clave-valor
console.log(Object.entries(countries));

// Resultado:
// [
//     ['MX', 'Mexico'],
//     ['CO', 'Colombia'],
//     ['CL', 'Chile'],
//     ['PE', 'Peru']
// ]

