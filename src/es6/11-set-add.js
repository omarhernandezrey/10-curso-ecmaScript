// Creación de un nuevo Set llamado 'list'
const list = new Set();

// Agregar elementos al Set utilizando el método 'add'
list.add('item 1'); // Agrega 'item 1' al Set
list.add('item 2').add('item 3'); // Agrega 'item 2' y 'item 3' al Set en una cadena

// Imprimir el contenido del Set 'list'
console.log(list);
