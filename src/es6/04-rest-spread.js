// Arrays destructuring
let fruits = ['Apple', 'Banana'];

// Desestructuración de un array en variables 'a' y 'b'
let [a, b] = fruits;
console.log(a, fruits[1]); // Imprime 'Apple Banana'

// Object destructuring
let user = {
    username: 'Oscar',
    age: 34
};

// Desestructuración de un objeto en variables 'username' y 'age'
let {username, age} = user;
console.log(username, user.age); // Imprime 'Oscar 34'



